  /// Returns a shuffled list of [count] words from the given categories and language.
  List<Word> getWords({
    required List<String> categoryIds,
    required String language,
    required int count,
  }) {
    final lang = _wordData.containsKey(language) ? language : 'en';
    final allWords = <Word>[];
    var idCounter = 0;

    for (final categoryId in categoryIds) {
      final categoryWords =
          _wordData[lang]?[categoryId] ?? _wordData['en']?[categoryId] ?? [];
      for (final entry in categoryWords) {
        allWords.add(Word(
          id: '${categoryId}_${lang}_${idCounter++}',
          text: entry['text'] as String,
          categoryId: categoryId,
          language: lang,
          difficulty: WordDifficulty.values.byName(entry['difficulty'] as String),
        ));
      }
    }

    allWords.shuffle(Random());

    if (count >= allWords.length) return allWords;
    return allWords.sublist(0, count);
  }

